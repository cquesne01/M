    private void ValiderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ValiderMouseClicked
        // TODO add your handling code here:
        
        //Erreur
        
        int erreur =0;
        erreurPos.setText("");
        erreurP1.setText("");
        erreurP2.setText("");
        
        //Variables a recuperer
        
        int tri=Integer.parseInt(triangle.getText());
        int p1=Integer.parseInt(P1.getText());
        int p2=Integer.parseInt(P2.getText());
        double pos=Double.parseDouble(position.getText());
        
        //Points
        
        Triangle triangle = Treillis.RecupTriangle(tri);
        double xP1 = triangle.getx(p1);
        double yP1 = triangle.gety(p1);
        double xP2 = triangle.getx(p2);
        double yP2 = triangle.gety(p2);
        double x = (xP2-xP1)*pos;
        double y = (yP2-yP1)*pos;
        
        //Angles
        
        double longueurP1X = xP1-x;
        double longueurP1Y = yP1-y;
        double angleP1 = Math.abs(Math.atan(longueurP1Y/longueurP1X)); //angle que forme le segment de triangle avec x
        
        //Forces
        
        String nomForce = "R" + Treillis.idNoeud;
        
        //Programme
        
        if (pos<0 || pos>0){
            erreur+=1;
            erreurPos.setText("Ce n'est pas entre 0 et 1 !");
        }
        
        if (erreur==0){
            
            //Noeud
            
            NoeudAppuiSimple noeudF = new NoeudAppuiSimple(Treillis.idNoeud, tri, p1, p2, pos,x,y);
            Treillis.AjouteNoeud(noeudF);
            
            //Forces
            
            Treillis.nbInconnues+=1;
            Treillis.AjouteInconnue(nomForce);
            Forces force = new Forces(nomForce, Treillis.idNoeud, Math.cos(Math.PI/2-angleP1), Math.sin(Math.PI/2-angleP1));
            Treillis.AjouteForce(force);
            
            //Matrice
            
            if (Treillis.idNoeud==1){
                Treillis.matrix.m.set(0, 6, Math.cos(Math.PI/2-angleP1)); //sur x
                Treillis.matrix.m.set(1, 6, Math.sin(Math.PI/2-angleP1)); //sur y
            }
            
            if (Treillis.idNoeud==2){
                Treillis.matrix.m.set(2, 6, Math.cos(Math.PI/2-angleP1)); //sur x
                Treillis.matrix.m.set(3, 6, Math.sin(Math.PI/2-angleP1)); //sur y
            }
            
            if (Treillis.idNoeud==3){
                Treillis.matrix.m.set(4, 6, Math.cos(Math.PI/2-angleP1)); //sur x
                Treillis.matrix.m.set(5, 6, Math.sin(Math.PI/2-angleP1)); //sur y
            }
            
            Treillis.idNoeud+=1;
            this.setVisible(false);
        }
    }//GEN-LAST:event_ValiderMouseClicked

