    private void validerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_validerMouseClicked
        // TODO add your handling code here:
        int erreurs=0;
        erreurXP1.setText("");
        erreurYP1.setText("");
        erreurXP2.setText("");
        erreurYP2.setText("");
        erreurXP3.setText("");
        erreurYP3.setText("");
        double xp1=Double.parseDouble(xP1.getText());
        double yp1=Double.parseDouble(yP1.getText());
        double xp2=Double.parseDouble(xP2.getText());
        double yp2=Double.parseDouble(yP2.getText());
        double xp3=Double.parseDouble(xP3.getText());
        double yp3=Double.parseDouble(yP3.getText());
        
        if (xp1>Terrain.xMax || xp1<Terrain.xMin ){
            erreurs+=1;
            xP1.setText("");
            erreurXP1.setText("X1 sort du terrain !");
        }
        
        if (yp1>Terrain.yMax || yp1<Terrain.yMin ){
            erreurs+=1;
            yP1.setText("");
            erreurYP1.setText("Y1 sort du terrain !");
        }
        
        if (xp2>Terrain.xMax || xp2<Terrain.xMin ){
            erreurs+=1;
            xP2.setText("");
            erreurXP2.setText("X2 sort du terrain !");
        }
        
        if (yp2>Terrain.yMax || yp2<Terrain.yMin ){
            erreurs+=1;
            yP2.setText("");
            erreurYP2.setText("Y2 sort du terrain !");
        }
        
        if (xp3>Terrain.xMax || xp3<Terrain.xMin ){
            erreurs+=1;
            xP3.setText("");
            erreurXP3.setText("X3 sort du terrain !");
        }
        
        if (yp3>Terrain.yMax || yp3<Terrain.yMin ){
            erreurs+=1;
            yP3.setText("");
            erreurYP3.setText("Y3 sort du terrain !");
        }
        
        if (erreurs!=0){
            Erreur.setText("Au moins l'un des points se trouve en dehors du terrain");
        }
        else {
            Erreur.setText("");
            Triangle triangleF = new Triangle(Treillis.idTriangle,Triangle.xP1,Triangle.yP1,Triangle.xP2,Triangle.yP2,Triangle.xP3,Triangle.yP3);
            Treillis.AjouteTriangle(triangleF);
            Treillis.idTriangle+=1;
            this.setVisible(false);
        }
    }//GEN-LAST:event_validerMouseClicked

